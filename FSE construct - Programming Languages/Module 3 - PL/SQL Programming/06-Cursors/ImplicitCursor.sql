SET SERVEROUTPUT ON

BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE EMPLOYEE_TEST';
EXCEPTION
    WHEN OTHERS THEN
        NULL;
END;
/

CREATE TABLE EMPLOYEE_TEST (
    emp_id   NUMBER,
    emp_name VARCHAR2(50),
    salary   NUMBER
);

INSERT INTO EMPLOYEE_TEST VALUES (1, 'Alice', 50000);
INSERT INTO EMPLOYEE_TEST VALUES (2, 'Bob', 60000);
INSERT INTO EMPLOYEE_TEST VALUES (3, 'Carol', 55000);
COMMIT;

DECLARE
    v_row_count NUMBER;
BEGIN
    DBMS_OUTPUT.PUT_LINE('--- Implicit Cursor Demo ---');
    DBMS_OUTPUT.PUT_LINE('');

    UPDATE EMPLOYEE_TEST
    SET salary = salary + 5000
    WHERE emp_id = 1;

    DBMS_OUTPUT.PUT_LINE('After UPDATE (emp_id = 1):');
    DBMS_OUTPUT.PUT_LINE('SQL%FOUND    : ' || CASE WHEN SQL%FOUND THEN 'TRUE' ELSE 'FALSE' END);
    DBMS_OUTPUT.PUT_LINE('SQL%NOTFOUND : ' || CASE WHEN SQL%NOTFOUND THEN 'TRUE' ELSE 'FALSE' END);
    DBMS_OUTPUT.PUT_LINE('SQL%ROWCOUNT : ' || SQL%ROWCOUNT);
    DBMS_OUTPUT.PUT_LINE('SQL%ISOPEN   : ' || CASE WHEN SQL%ISOPEN THEN 'TRUE' ELSE 'FALSE' END);
    DBMS_OUTPUT.PUT_LINE('');

    DELETE FROM EMPLOYEE_TEST
    WHERE emp_id = 99;

    DBMS_OUTPUT.PUT_LINE('After DELETE (emp_id = 99 - does not exist):');
    DBMS_OUTPUT.PUT_LINE('SQL%FOUND    : ' || CASE WHEN SQL%FOUND THEN 'TRUE' ELSE 'FALSE' END);
    DBMS_OUTPUT.PUT_LINE('SQL%NOTFOUND : ' || CASE WHEN SQL%NOTFOUND THEN 'TRUE' ELSE 'FALSE' END);
    DBMS_OUTPUT.PUT_LINE('SQL%ROWCOUNT : ' || SQL%ROWCOUNT);
    DBMS_OUTPUT.PUT_LINE('');

    UPDATE EMPLOYEE_TEST
    SET salary = salary + 1000;

    DBMS_OUTPUT.PUT_LINE('After UPDATE (all employees):');
    DBMS_OUTPUT.PUT_LINE('SQL%ROWCOUNT : ' || SQL%ROWCOUNT);
END;
/

DROP TABLE EMPLOYEE_TEST;
